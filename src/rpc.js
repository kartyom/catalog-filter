/**
 * @param {string} query
 * @param {Filters} filters
 * @param {number} offset
 * @param {number} limit
 */
export async function findItems(query, filters, offset, limit = LIMIT) {
  await sleep(1500);

  const priceFilter = isPriceInRange(filters.price);
  const ratingFilter = isRatingInRange(filters.rating);
  const categoryFilter = isCategorySame(filters.category);
  const brandFilter = isBrandSame(filters.brand);
  const queryFilter = isQuerySimilar(query);

  return data
    .filter(priceFilter)
    .filter(ratingFilter)
    .filter(categoryFilter)
    .filter(brandFilter)
    .filter(queryFilter)
    .slice(Number(offset), Number(offset) + Number(limit));
}

/**
 * @param {Range | null} range
 */
function isPriceInRange(range) {
  /**
   * @param {Item} item
   */
  return function (item) {
    if (range === null) {
      return true;
    }

    if (item.price < range.start) {
      return false;
    }

    if (item.price > range.end) {
      return false;
    }

    return true;
  };
}

/**
 * @param {Range | null} range
 */
function isRatingInRange(range) {
  /**
   * @param {Item} item
   */
  return function (item) {
    if (range === null) {
      return true;
    }

    if (item.rating < range.start) {
      return false;
    }

    if (item.rating > range.end) {
      return false;
    }

    return true;
  };
}

/**
 * @param {string | null} category
 */
function isCategorySame(category) {
  /**
   * @param {Item} item
   */
  return function (item) {
    if (category === null) {
      return true;
    }

    return item.category === category;
  };
}

/**
 * @param {string | null} brand
 */
function isBrandSame(brand) {
  /**
   * @param {Item} item
   */
  return function (item) {
    if (brand === null) {
      return true;
    }

    return item.brand === brand;
  };
}

/**
 * @param {string} query
 */
function isQuerySimilar(query) {
  /**
   * @param {Item} item
   */
  return function (item) {
    if (query === "") {
      return true;
    }

    return item.name.toLocaleLowerCase().includes(query.toLocaleLowerCase());
  };
}

/**
 * @param {number} millis
 */
async function sleep(millis) {
  return new Promise((resolve) => {
    setTimeout(() => {
      resolve();
    }, millis);
  });
}

